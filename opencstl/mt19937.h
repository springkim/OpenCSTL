typedef double (*random_fn)(void);

typedef int64_t (*randint_fn)(int64_t, int64_t);

typedef char *(*uuid_fn)(void);

typedef struct {
    random_fn random;
    randint_fn randint;
    seed_fn seed;
    uuid_fn uuid;
} RANDOM;

