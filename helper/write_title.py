def check_text(text):
    allowed_chars = """
    ABCDEFGHIJKLMN
    OPQRSTUVWXYZ
    0123456789
    !#$%()-=+[]:;"'?/
    _
    """.strip().replace("\n", "")

    text_upper = text.upper()

    for c in text_upper:
        if c not in allowed_chars:
            raise Exception(f'{c} is not allowed')
    return text_upper


def main():
    text = input("Enter text: ")

    text = check_text(text)
    chars = []
    for c in text:
        message = messages[c]
        chars.append(message)

    cmds = []
    for i in range(6):
        line = ''
        for char in chars:
            line += char[i]
        cmds.append(f'// {line}')

    print("\n".join(cmds))


if __name__ == '__main__':
    main()
