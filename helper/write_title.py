def getOS():
    OS = None
    match platform.system():
        case 'Windows':
            OS = 'Windows'
        case 'Darwin':
            OS = 'macOS'
        case 'Linux':
            OS = 'Linux'
        case _:
            raise Exception('Unsupported OS')
    print(f'Detected OS: {OS}')
    return OS


